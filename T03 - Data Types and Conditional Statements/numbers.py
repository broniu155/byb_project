# Define three variables by user

int_one = int(input("Enter first integer value:"))
int_two = int(input("Enter second integer value:"))
int_three = int(input("Enter third integer value:"))


# Sum and output all the input numbers

print(int_one + int_two + int_three)

# Output result of substract first input value from second

print(int_one - int_two)

# Output result of third number multiplied by first

print(int_three * int_one)

# Output result of the Sum of all values and divide by third

print((int_one + int_two + int_three) * int_three)

